package day01variables;

public class Variables01 {

	public static void main(String[] args) {
		//if you use println, Java will print then navigate to the next line
		System.out.println("Hello Everyone ");
		System.out.println("Hello World");
		
		//If you use print, Java will print and wait in the same line
		System.out.print("Hello Everyone");
		System.out.println(" Hello World");
		
		//Create a boolean variable and print it on the console
		//If you use "" Java prints the text directly on the console, if you
		//Do not use "" Java prints the value of the Variable on the console
		boolean retired = true;
		System.out.println(retired);
		
		boolean fat = false;
		System.out.println(fat);
		
		boolean javaHard = false;
		System.out.println(javaHard);
		
		//Create an integer variable and print its value on the console
		
		int age = 19;
		System.out.println(age);
		
		//Create a float variable and print it on the console
		float weight = 70.5f; 
		System.out.println(weight);
		
		//Create a double variable and print it on the console
		double price = 20.567;
		System.out.println(price);
		
		double pi = 3.14159265359;
		System.out.println(pi);
		
		//Create a char variable and print it on the console
		char initial = '5';
		System.out.println(initial);	
		
		//Create a string variable and print it on the console
		String myName = "Enes Sertkan";
		System.out.println(myName);
		
	}
	
	
}
